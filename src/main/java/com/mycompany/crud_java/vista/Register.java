    private void btn_registrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_registrarActionPerformed
        // TODO add your handling code here:
         // TODO add your handling code here:
        String email=jtext_email.getText().trim();
        String password=jtext_password.getText().trim();
        String nombre=jtext_nombre.getText().trim();
        String imagen=pathImage;
        
        
        if(!nombre.isEmpty() || !email.isEmpty() && !password.isEmpty()){
            int resultado=0;
            
            if(!patternMatches(email, "^(.+)@(.+)$")){ 
                System.out.println("Email no vÃ¡lido");
                JOptionPane.showMessageDialog(null, "Email no vÃ¡lido");
                
            }else{
                String pass=AutenticateController.getSHA256(password);
                System.out.println(pass);
                
                String sql="INSERT INTO usuarios (nombre, email, password, imagen) VALUES (?,?,?,?)";

                try {
                    con = conexion.getConnection();
                    ps = con.prepareStatement(sql);            
                    ps.setString(1,nombre);
                    ps.setString(2,email);
                    ps.setString(3,pass);
                    ps.setString(4,imagen);
                   
                    resultado=ps.executeUpdate();   

                    if(resultado==1){
                        JOptionPane.showMessageDialog(null, "usuario agregado con Ã©xito");
                        
                        jtext_nombre.setText("");
                        jtext_email.setText("");
                        jtext_password.setText("");
                    }
                    else{
                         JOptionPane.showMessageDialog(null, "Error");
                    }
                } catch (Exception e) {
                     System.err.println("Error: "+e);
                    e.printStackTrace();
                }finally{
                    try {
                        ps.close();
                        con.close();
                    } catch (SQLException ex) {
                        System.err.println("Error: "+ex);
                    }

                }
            }
        
            
        }else{
             JOptionPane.showMessageDialog(null, "Error: existen campos vacios");  
             
        }
    }//GEN-LAST:event_btn_registrarActionPerformed

