    public void setUsuario(Usuario user) {
	this.usuario=user;
        
         if(usuario.getImagen()!=null && !usuario.getImagen().isEmpty()){
                jlabel_usuario.setText("Bienvenido " + usuario.getNombre());
                File finput = new File(usuario.getImagen());
                System.out.println("Ruta vista: "+usuario.getImagen());
                Image image = null;
            try {
                image = ImageIO.read(finput);
                image = image.getScaledInstance(80, 80, Image.SCALE_SMOOTH);
                jLabelFoto.setIcon(new ImageIcon(image));
            } catch (IOException ex) {
                System.err.println("Error: "+ex);
                ex.printStackTrace();
            } catch(Exception e){
                 System.err.println("Error: "+e);
                 e.printStackTrace();
            }
                
            
        }
    }

