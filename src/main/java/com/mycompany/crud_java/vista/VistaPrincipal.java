    private void tablaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablaMouseClicked
         int fila = tabla.getSelectedRow();
            if (fila == -1) {
                JOptionPane.showMessageDialog(null, "Debe seleccionar una fila");
            } else {
                int id = Integer.parseInt((String) tabla.getValueAt(fila, 0).toString());
                String nom = (String) tabla.getValueAt(fila, 1);
                String tel = (String) tabla.getValueAt(fila, 2);
                String correo = (String) tabla.getValueAt(fila, 3);
                
                text_id.setText("" + id);
                text_nombre.setText(nom);
                text_correo.setText(correo);
                text_telefono.setText(tel);
            }
    }//GEN-LAST:event_tablaMouseClicked

