    private void btn_imageActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_imageActionPerformed
        // TODO add your handling code here:
        JFileChooser jc = new JFileChooser();
        FileNameExtensionFilter filter = new FileNameExtensionFilter("Archivos de imagen", "jpg","png","bmp");
        jc.setFileFilter(filter);
        int result = jc.showOpenDialog(this);
        
        if (result == JFileChooser.APPROVE_OPTION)
        {
            File finput = jc.getSelectedFile();
            System.out.println("Ruta: "+finput.getAbsolutePath());
            System.out.println("Nombre: "+finput.getName());
            label_foto.setText(finput.getAbsolutePath());
            pathImage="./imagenes/"+finput.getName();
            try {
                FileInputStream fis=new FileInputStream(finput);
                File foutput=new File(pathImage);
                FileOutputStream fos = new FileOutputStream(foutput);
                byte[] buffer=new byte[1024*100];
                int len;
               try {
                    while ((len = fis.read(buffer)) > 0) {
                     fos.write(buffer, 0, len);
                    }
               } catch (IOException ex) {
                    System.err.println("Error: "+ex);
                    ex.printStackTrace();
               }
            } catch (FileNotFoundException ex) {
                System.err.println("Error: "+ex);
                ex.printStackTrace();
            }
            
            
            try
            {
                Image image = ImageIO.read(finput);
                image = image.getScaledInstance(100, 100, Image.SCALE_SMOOTH);
                label_foto.setIcon(new ImageIcon(image));
                
            }
            catch (Exception e)
            {
                System.err.println("Error: "+e);
                e.printStackTrace();
            }
        } else if (result == JFileChooser.CANCEL_OPTION)
        {
            label_foto.setText("No se seleccionÃ³ archivo");
        }
    }//GEN-LAST:event_btn_imageActionPerformed

